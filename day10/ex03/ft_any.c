int		ft_any(char **tab, int (*f)(char *))
{
	int i;
	int x;
	int flag;

	i = 0;
	x = -1;
	flag = 0;
	while (tab[i])
	{
		x = f(tab[i]);
		((x == 1) ? (flag = 1) : (x++));
		i++;
	}
	return (flag);
}
