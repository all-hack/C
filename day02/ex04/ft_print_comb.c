int		ft_putchar(char c);

void	ft_print_comb(void)
{
	int a[3];

	a[0] = 0;
	while (a[0] < 10)
	{
		a[1] = a[0] + 1;
		while (a[1] < 10)
		{
			a[2] = a[1] + 1;
			while (a[2] < 10)
			{
				ft_putchar(a[0] + '0');
				ft_putchar(a[1] + '0');
				ft_putchar(a[2] + '0');
				if (a[0] != 7)
					ft_putchar(',');
				if (a[0] != 7)
					ft_putchar(' ');
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
